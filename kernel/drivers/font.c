#define GLYPH_COUNT (sizeof(glyphs) / sizeof(glyphs[0]))

static const char *blank_rows[7] = {".....",".....",".....",".....",".....",".....","....."};

const char* const* font_get_glyph(char c) {
    if (c >= 'a' && c <= 'z') c = c - 'a' + 'A'; /* map lowercase to uppercase */
    for (unsigned i = 0; i < GLYPH_COUNT; i++) {
        if (glyphs[i].ch == c) return glyphs[i].rows;
    }
    return blank_rows;
}
